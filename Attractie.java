abstract class Attractie {

    String naam;
    float prijs;
    int oppervlakte;
    double omzet;

    void draaien(){
    }

    public String getNaam() {
        return naam;
    }

    public double getPrijs() {
        return prijs;
    }

    public int getOppervlakte() {
        return oppervlakte;
    }

    public double getOmzet() {
        return omzet;
    }

}
